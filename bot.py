# دالة لإرسال الملفات
async def send_file(update: Update, file_id: str, caption: str = None):
    try:
        await update.message.reply_audio(file_id, caption=caption)
    except:
        try:
            await update.message.reply_video(file_id, caption=caption)
        except:
            await update.message.reply_document(file_id, caption=caption)


# دالة للحصول على file_id
async def get_file_id(update: Update, context: ContextTypes.DEFAULT_TYPE):
    m = update.message
    if m.audio:
        await m.reply_text(m.audio.file_id)
    elif m.video:
        await m.reply_text(m.video.file_id)
    elif m.voice:
        await m.reply_text(m.voice.file_id)
    elif m.document:
        await m.reply_text(m.document.file_id)
    elif m.photo:
        await m.reply_text(m.photo[-1].file_id)


