async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("اختر من القائمة:", reply_markup=main_menu())

async def handle_text(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text.strip()
    user_data = context.user_data

    # الرئيسية
    if text == "🏠 الرئيسية":
        user_data.clear()
        return await update.message.reply_text("رجعت للقائمة الرئيسية:", reply_markup=main_menu())

    # الدخول على قسم رئيسي
    if text in content.keys():
        user_data["current_section"] = text
        user_data.pop("current_item", None)
        user_data.pop("current_subitem", None)
        return await update.message.reply_text(f"اختر من {text}:", reply_markup=section_menu(text))

    # زر رجوع
    if text == "↩️ رجوع":
        if "current_subitem" in user_data:
            # إذا كان داخل تصنيف فرعي -> يرجع للشيخ
            section = user_data["current_section"]
            item = user_data["current_item"]
            user_data.pop("current_subitem", None)
            keyboard = [[sub] for sub in content[section][item].keys()]
            keyboard.append(["↩️ رجوع", "🏠 الرئيسية"])
            return await update.message.reply_text(f"اختر تصنيف من {item}:", reply_markup=ReplyKeyboardMarkup(keyboard, resize_keyboard=True))

        elif "current_item" in user_data:
            # إذا كان داخل شيخ -> يرجع للقسم
            section = user_data["current_section"]
            user_data.pop("current_item", None)
            return await update.message.reply_text(f"اختر من {section}:", reply_markup=section_menu(section))

        elif "current_section" in user_data:
            # إذا كان داخل قسم -> يرجع للقائمة الرئيسية
            user_data.pop("current_section", None)
            return await update.message.reply_text("رجعت للقائمة الرئيسية:", reply_markup=main_menu())

# عشوائي لتلاوة القرآن الكريم
    if text == "عشوائي 🌀" and user_data.get("current_section") == "تلاوة قرآن كريم":
        all_files = [
            (qari, idx, f)
            for qari, data in content["تلاوة قرآن كريم"].items()
            for idx, f in enumerate(data.get("files", []))
        ]
        if not all_files:
            return await update.message.reply_text("❌ لا يوجد أي مقاطع تلاوة حالياً.")
        qari, idx, file_id = random.choice(all_files)
        captions = content["تلاوة قرآن كريم"][qari].get("captions", [])
        caption = captions[idx] if idx < len(captions) and captions[idx] else f"🎁 تلاوة عشوائية من {qari}:"
        return await send_file(update, file_id, caption)

    # عشوائي لمحاضرات/بودكاستات
    if text == "عشوائي 🎲" and user_data.get("current_section") == "محاضرات/بودكاستات متنوعة":
        all_files = [
            (name, idx, f)
            for name, data in content["محاضرات/بودكاستات متنوعة"].items()
            for idx, f in enumerate(data.get("files", []))
        ]
        if not all_files:
            return await update.message.reply_text("❌ لا يوجد أي مقاطع حالياً.")
        sheikh, idx, file_id = random.choice(all_files)
        captions = content["محاضرات/بودكاستات متنوعة"][sheikh].get("captions", [])
        caption = captions[idx] if idx < len(captions) and captions[idx] else f"🎁 مقطع عشوائي من {sheikh}:"
        return await send_file(update, file_id, caption)

    # عشوائي للمقاطع المسجلة
    if text == "عشوائي 🔀" and user_data.get("current_section") == "مقتطفات متنوعة":
        all_files = [
            (sheikh, idx, f)
            for sheikh, data in content["مقتطفات متنوعة"].items()
            for idx, f in enumerate(data.get("files", []))
        ]
        if not all_files:
            return await update.message.reply_text("❌ لا يوجد أي ملفات حالياً.")
        sheikh, idx, file_id = random.choice(all_files)
        captions = content["مقتطفات متنوعة"][sheikh].get("captions", [])
