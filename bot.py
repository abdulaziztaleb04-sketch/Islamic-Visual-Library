def section_menu(section_name):
    section = content.get(section_name, {})
    keyboard = []

    if section_name == "مقتطفات متنوعة":
        keyboard.append(["عشوائي 🔀"])

    if section_name == "محاضرات/بودكاستات متنوعة":
        keyboard.append(["عشوائي 🎲"])

    if section_name == "تلاوة قرآن كريم":
        keyboard.append(["عشوائي 🌀"])   # زر جديد للتلاوة

    keyboard += [[name] for name in section.keys()]

    # زر رجوع + رئيسية بنفس السطر
    keyboard.append(["↩️ رجوع", "🏠 الرئيسية"])
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)
        
        
      

def files_menu(section, item, subitem=None):
    if subitem:
        files = content[section][item][subitem].get("files", [])
        names = content[section][item][subitem].get("names", [])
    else:
        files = content[section][item].get("files", [])
        names = content[section][item].get("names", [])

    keyboard = []
    row = []

    # إذا في أسماء بنفس عدد الملفات -> استعمل الأسماء بدل الأرقام
    use_names = names and len(names) == len(files)

    for i in range(len(files)):
        label = names[i] if use_names else str(i+1)
        row.append(label)
        if len(row) == 5:  # كل 4 أزرار بسطر واحد
            keyboard.append(row)
            row = []
    if row:
        keyboard.append(row)

    keyboard.append(["↩️ رجوع", "🏠 الرئيسية"])
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)


